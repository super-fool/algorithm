/**
 * 最大连续为1的个数
 */

var longOnes = function (arr, k) {
  let len = arr.length;
  let maxOneLength = 0;
  let r = 0;
  let l = 0;
  let miK = k; // 剩余K
  while (r < len) {
    if (arr[r] === 0) {
      if (miK > 0) {
        r++;
        miK--;
      } else {
        if (arr[l] === 0) {
          miK++;
        }
        l++;
        continue;
      }
    } else {
      r++;
    }
    maxOneLength = Math.max(maxOneLength, r - l);
  }
  return maxOneLength;
};

